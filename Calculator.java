public class Calculator {

    // Group 1: Addition (Members: John, Sarah, Alex)
    public static int add(int a, int b) {
        return a + b;
    }

    // Group 3: Multiply (Members: De Belen, Magusib, M. Conde, Apolonio, Fabian, Servandil,
    // Alava, Valdez, Briones, Destajo, Bargaso)
    public static int mul(int a, int b) {
        return a * b;
    }

    // Group 10: Equality Check
    public static boolean isEqual(int a, int b) {
        return a == b;
    }

    // Group 9: Minimum (Members: Jarap, Coronado)
    public static int min(int a, int b) {
        return (a < b) ? a : b;
    }
}
